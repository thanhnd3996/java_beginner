public class JavaDataType {
    public static void main(String[] args) throws Exception {
        // integer types
        byte aSingleByte = 127;
        short aSmallNumber = 20000;
        int anInteger = 2147483647;
        long aLargeNumber = 1213121314654446464L;

        // decimal types
        double aDouble = 1.86;
        float aFloat = 1.86f;

        // booleans
        boolean isHandsome = true;

        // character
        char copyrightSymbol = '\u00A9';

        System.out.println(aSingleByte);
        System.out.println(aSmallNumber);
        System.out.println(anInteger);
        System.out.println(aLargeNumber);
        System.out.println(aDouble);
        System.out.println(aFloat);
        System.out.println(isHandsome);
        System.out.println(copyrightSymbol);

    }
}
